CREATE TABLE IF NOT EXISTS `usr` (
  `uid` int(11) NOT NULL AUTO_INCREMENT,
  `fb_id` varchar(65) NOT NULL,
  `source` varchar(64) NOT NULL,
  `email` varchar(256) DEFAULT NULL,
  `firstname` varchar(64) DEFAULT NULL,
  `lastname` varchar(64) DEFAULT NULL,
  `birthdate` date NOT NULL,
  `civility` varchar(5) NOT NULL,
  `address` varchar(128) NOT NULL,
  `postcode` varchar(10) NOT NULL,
  `city` varchar(64) NOT NULL,
  `country` varchar(32) NOT NULL,
  `lang` varchar(12) NOT NULL,
  `telephone` varchar(16) NOT NULL,
  `passport_valid` int(1) NOT NULL,
  `photo_full` text NOT NULL,
  `photo_portrait` text NOT NULL,
  `personality` text NOT NULL,
  `accept_terms` int(1) NOT NULL,
  `created_at` datetime NOT NULL,
  `updated_at` datetime NOT NULL,
  `submited_at` datetime NOT NULL,
  `starred` int(1) NOT NULL,
  PRIMARY KEY (`fb_id`),
  KEY `email` (`email`),
  KEY `firstname` (`firstname`),
  KEY `lastname` (`lastname`),
  KEY `birthdate` (`birthdate`),
  KEY `civility` (`civility`),
  KEY `address` (`address`),
  KEY `city` (`city`),
  KEY `postcode` (`postcode`),
  KEY `country` (`country`),
  KEY `telephone` (`telephone`),
  KEY `passport_valid` (`passport_valid`),
  KEY `accept_terms` (`accept_terms`),
  KEY `created_at` (`created_at`),
  KEY `updated_at` (`updated_at`),
  KEY `submited_at` (`submited_at`),
  KEY `uid` (`uid`),
  KEY `lang` (`lang`),
  KEY `source` (`source`),
  KEY `starred` (`starred`)
) ENGINE=MyISAM DEFAULT CHARSET=utf8 AUTO_INCREMENT=1 ;
